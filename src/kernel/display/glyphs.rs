pub fn get_glyph(c: char) -> &'static [i32] {
    match c {
        '0' => &ZERO,
        '1' => &ONE,
        '2' => &TWO,
        '3' => &THREE,
        '4' => &FOUR,
        '5' => &FIVE,
        '6' => &SIX,
        '7' => &SEVEN,
        '8' => &EIGHT,
        '9' => &NINE,
        'A' | 'a' => &A,
        'B' | 'b' => &B,
        'C' | 'c' => &C,
        'D' | 'd' => &D,
        'E' | 'e' => &E,
        'F' | 'f' => &F,
        'G' | 'g' => &G,
        'H' | 'h' => &H,
        'I' | 'i' => &I,
        'J' | 'j' => &J,
        'K' | 'k' => &K,
        'L' | 'l' => &L,
        'M' | 'm' => &M,
        'N' | 'n' => &N,
        'O' | 'o' => &O,
        'P' | 'p' => &P,
        'R' | 'r' => &R,
        'S' | 's' => &S,
        'T' | 't' => &T,
        'U' | 'u' => &U,
        'W' | 'w' => &W,
        'X' | 'x' => &X,
        'Y' | 'y' => &Y,
        'Z' | 'z' => &Z,
        _ => &MISSING,
    }
}

pub fn get_glyph_width(c: char) -> i32 {
    match c {
        'i' | 'I' | ' ' => 2,
        'l' | 'L' => 4,
        'm' | 'M' | 't' | 'T' | 'w' | 'W' | 'x' | 'X' | 'y' | 'Y' => 6,
        _ => 5,
    }
}

